public class escapechar{
    public static void main(String[]args)
    {
        System.out.println("puram\n meghana");//(\n next line)
        System.out.println("puram\tmeghana"); //(\t  long space)
System.out.println("puram\"meghana\"");
System.out.println("puram \'meghana\'");
System.out.println("puram\\meghana\\");
System.out.println("puram\b");//(\b backspace)

    }
}
